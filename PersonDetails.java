package pack;

public class PersonDetails {

    protected int id;
    protected int age;
    protected String name;

    public PersonDetails(int id, int age, String name) {
        this.id = id;
        this.age = age;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        String string = "id: " + id + " age: " + age + " name: " + name;
        return string;
    }
}
